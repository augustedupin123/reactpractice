var shirt_sizes = ["S","M","L","XL"];

for(var i=0; i<shirt_sizes.length; i++){
    if(shirt_sizes[i] == "S"){
        console.log("Small");
    } else if(shirt_sizes[i] == "M"){
        console.log("Medium");
    } else if(shirt_sizes[i] == "L"){
        console.log("Large");
    } else if(shirt_sizes[i] == "XL"){
        console.log("Extra Large");
    }

}
