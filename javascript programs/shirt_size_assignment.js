var S = "Small";
var M = "medium";
var L = "Large";
var XL = "Extra Large";

var char = window.prompt("Enter the size of the shirt");
switch(char) {
    case "S":
        console.log("Small");
    case "M":
        console.log("Medium");
    case "L":
        console.log("Large");
    case "XL":
        console.log("Extra Large");
}

